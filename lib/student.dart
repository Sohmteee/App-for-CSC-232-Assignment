class Student {
  String firstName;
  String lastName;
  String sex;
  String department;
  String stateOfOrigin;
  String email;
  int regNumber;
  String phoneNumber;

  Student({
    required this.firstName,
    required this.lastName,
    required this.sex,
    required this.department,
    required this.stateOfOrigin,
    required this.regNumber,
    required this.phoneNumber,
    required this.email,
  });
}
